package loops_iterations_repetitions.ushtrime;

public class Deyra3 {
    public static void main(String[] args) {
        char shkronja = 'A';
        while (shkronja <= 'Z') {
            System.out.printf("%c%n", shkronja++);
        }

        for (char i = 'a'; i <= 'z'; i++) {
            System.out.printf("%c%n", i);
        }
    }
}























