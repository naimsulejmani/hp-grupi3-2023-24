package loops_iterations_repetitions.for_loop;

public class ForExample1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("I am so smart! " + i);
        }
        System.out.println("S-M-R-T");
        System.out.println("I mean S-M-A-R-T");
    }
}





















