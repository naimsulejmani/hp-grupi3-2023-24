package other_operators;

public class BitWiseExample {
    public static void main(String[] args) {
        System.out.println(42 & 15);
        System.out.println(42 | 15);
        System.out.println(42 ^ 15);

        byte x = 1;
        System.out.println(x << 1);
        System.out.println(x << 2);
        System.out.println(x << 3);
        System.out.println(55 << 2);
        System.out.println(55 >> 2);
    }
}














